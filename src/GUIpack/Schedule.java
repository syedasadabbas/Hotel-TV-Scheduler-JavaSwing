    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String title = Title.getText();
        String genre = Genre.getText();
        String duration = Duration.getText();
        String actor = Actor.getText();
        String director = Director.getText();
        String synopsis = Synopsis.getText();
        String channel = (String) Channel.getSelectedItem();
        String time = (String) Time.getSelectedItem();
    
    if (title.isEmpty() || genre.isEmpty() || duration.isEmpty() || actor.isEmpty() || director.isEmpty() || synopsis.isEmpty() || channel.isEmpty() || time.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
                                      "Please Enter All Fields",
                                      "Try Again!",
                                      JOptionPane.ERROR_MESSAGE);   
    } else {
        if ("Channel 1".equals(channel)) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(new Object[] {time, title});
        } else if ("Channel 2".equals(channel)) {
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.addRow(new Object[] {time, title});
        }
        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        model.addRow(new Object[] {genre, title, duration});
        
        Title.setText("");
        Genre.setText("");
        Synopsis.setText("");
        Actor.setText("");
        Director.setText("");
        Duration.setText("");
        Rentable.setSelected(false);
            
        }


    }//GEN-LAST:event_jButton1ActionPerformed

