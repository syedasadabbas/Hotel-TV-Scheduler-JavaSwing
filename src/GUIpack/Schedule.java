    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try{
            FileInputStream file = new FileInputStream("file.bin");
            ObjectInputStream input = new ObjectInputStream(file);
            
            FileInputStream file1 = new FileInputStream("file1.bin");
            ObjectInputStream input1 = new ObjectInputStream(file1);
            
            FileInputStream file2 = new FileInputStream("file2.bin");
            ObjectInputStream input2 = new ObjectInputStream(file2);
            
            Vector<Vector> tableData = (Vector<Vector>)input.readObject();
            Vector<Vector> tableData1 = (Vector<Vector>)input.readObject();
            Vector<Vector> tableData2 = (Vector<Vector>)input.readObject();
            
            input.close();
            file.close();
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
            DefaultTableModel model1 = (DefaultTableModel) jTable2.getModel();
        
            DefaultTableModel model2 = (DefaultTableModel) jTable3.getModel();
            
            for(int i = 0; i < tableData.size(); i++){
                Vector row = tableData.get(i);
                model.addRow(new Object[]{row.get(1), row.get(2)});
            }
            for(int i = 0; i < tableData1.size(); i++){
                Vector row = tableData1.get(i);
                model1.addRow(new Object[]{row.get(1), row.get(2)});
            }
            for(int i = 0; i < tableData2.size(); i++){
                Vector row = tableData2.get(i);
                model2.addRow(new Object[]{row.get(1), row.get(2), row.get(3)});
            }
            
        } catch (Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

